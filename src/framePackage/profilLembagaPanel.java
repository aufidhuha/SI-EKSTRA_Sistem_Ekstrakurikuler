    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
        // TODO add your handling code here:
        if (txtFormNamaLembaga.getText().isBlank() || txaFormAlamat.getText().isBlank() || txtFormKodePos.getText().isBlank() || txtFormTelepon.getText().isBlank() || txtFormEmail.getText().isBlank()) {
            JOptionPane.showMessageDialog(null, "Harap mengisi data secara keseluruhan");
            return;
            
        }
        
        profilClass profil = new profilClass();
        
        profil.setNama_lembaga(txtFormNamaLembaga.getText());
        profil.setAlamat(txaFormAlamat.getText());
        profil.setKodePos(txtFormKodePos.getText());
        profil.setTelepon(txtFormTelepon.getText());
        profil.setEmail(txtFormEmail.getText());
        
        profil.saveData();
        
        reset();
        tampilData();
    }//GEN-LAST:event_buttonSimpanActionPerformed

