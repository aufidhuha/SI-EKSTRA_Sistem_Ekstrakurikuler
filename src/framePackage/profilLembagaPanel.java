    void tampilData() {
        profilClass data = new profilClass();

        try {
            ResultSet rsVar = data.showData();

            if (rsVar.next()) {
                String namaLembaga = rsVar.getString("nama_lembaga");
                String alamat = rsVar.getString("alamat");
                String kodePos = rsVar.getString("kodepos");
                String telepon = rsVar.getString("telepon");
                String email = rsVar.getString("email");

                txtNamaLembaga.setText(namaLembaga);
                txaAlamat.setText(alamat);
                txtKodePos.setText(kodePos);
                txtTelepon.setText(telepon);
                txtEmail.setText(email);
                buttonHapus.setEnabled(true);
                
            } else {
                txtNamaLembaga.setText("-");
                txaAlamat.setText("-");
                txtKodePos.setText("-");
                txtTelepon.setText("-");
                txtEmail.setText("-");
                buttonHapus.setEnabled(false);
                
            }
        } catch (SQLException sQLException) {
            JOptionPane.showMessageDialog(null, "Error : " + sQLException.getMessage());
        }
    }

